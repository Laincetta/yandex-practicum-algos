"""
условие: https://contest.yandex.ru/contest/25597/problems/A/
ссылка на посылку: https://contest.yandex.ru/contest/25597/run-report/146348611/

-- ПРИНЦИП РАБОТЫ --

Я реализовал алгоритм вычисления расстояния Левенштейна с помощью динамического программирования.
Основная идея: на каждом шаге хранить минимальное количество действий
(вставок, удалений или замен), необходимых для превращения первых i символов строки s
в первые j символов строки t.

Для оптимизации по памяти вместо двумерной таблицы dp теперь используются
только две одномерные строки: prev и curr, соответствующие предыдущему и текущему значениям i.
Сначала инициализируется первая строка prev - базовый случай, когда одна из строк пустая:
количество шагов равно длине другой строки.
Затем в цикле по i заполняется curr, где для каждой пары символов
проверяется, совпадают ли они.
Если символы равны, берётся значение из prev[j - 1] (по диагонали).
Если различаются - выбирается минимум из трёх вариантов:
удалить символ из s, добавить символ из t или заменить один на другой,
добавляя к минимуму единицу.

После обработки строки curr становится новой prev для следующей итерации.
В конце в prev[len(t)] хранится минимальное количество операций,
необходимое для преобразования строки s в строку t.


-- ДОКАЗАТЕЛЬСТВО КОРРЕКТНОСТИ --

Алгоритм на каждом шаге поддерживает инвариант динамического программирования:
все значения prev и curr на текущий момент корректно содержат
минимальное количество операций, необходимых для приведения подстрок s[:i] и t[:j].

Базовый случай задаёт корректное начальное состояние инварианта -
число преобразований между пустой строкой и префиксом другой строки равно длине этого префикса.
При вычислении очередного значения curr[j] используются только элементы
prev[j], curr[j - 1] и prev[j - 1], которые уже посчитаны и соответствуют тем же правилам.

Выбор минимума среди трёх переходов (удаление, вставка, замена) гарантирует,
что в каждой позиции сохраняется оптимальное подрешение.
Следовательно, после обработки всех символов строк инвариант остаётся верным.
Значение prev[len(t)] содержит минимальное возможное число операций,
что и является искомым расстоянием Левенштейна.


-- ВРЕМЕННАЯ СЛОЖНОСТЬ --

По сути, решение сводится к заполнению таблицы размером S * T итераций,
где S и T - длины исходных строк.
Каждая итерация вычисляется за несколько константных операций,
так как используется только три соседних значения (prev[j], curr[j - 1], prev[j - 1]).
=> итоговая временная сложность алгоритма составляет:
O(S * T * 1) = O(S * T)


-- ПРОСТРАНСТВЕННАЯ СЛОЖНОСТЬ --

Вместо хранения всей матрицы dp размером S * T используется только две строки dp:
prev и curr, длиной (T + 1) каждая.
Таким образом, количество хранимых элементов пропорционально длине второй строки T.
=> итоговая пространственная сложность алгоритма: O(T + 1) = O(T)
"""


def get_levenshtein_dist(s: str, t: str) -> int:
    # теперь храним только две строки dp - для текущего и предыдущего i
    prev = list(range(len(t) + 1))
    curr = [0] * (len(t) + 1)

    for i in range(1, len(s) + 1):
        curr[0] = i

        for j in range(1, len(t) + 1):
            if s[i - 1] == t[j - 1]:
                curr[j] = prev[j - 1]
            else:
                curr[j] = 1 + min(
                    prev[j],     # s - s[i] (delete)
                    curr[j - 1], # s + t[j] (add)
                    prev[j - 1]  # s[i] <-> t[j] (replace)
                )

        prev, curr = curr, prev

    return prev[len(t)]


def main():
    s = input().strip()
    t = input().strip()
    levenshtein_dist = get_levenshtein_dist(s, t)
    print(levenshtein_dist)


if __name__ == '__main__':
    main()