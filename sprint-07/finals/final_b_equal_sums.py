"""
условие: https://contest.yandex.ru/contest/25597/problems/B/
ссылка на посылку: https://contest.yandex.ru/contest/25597/run-report/145858808/

-- ПРИНЦИП РАБОТЫ --

Я реализовал алгоритм проверки возможности разбиения множества чисел
на две части с одинаковой суммой с помощью динамического программирования.
Основная идея: построить массив dp, где элемент dp[j] показывает,
можно ли из некоторого подмножества i-тых чисел набрать сумму j.

Изначально достижима только сумма 0 (база динамики), так как пустое множество даёт ноль очков.
Далее будем расширять наше подмножество на i-ые элементы исходного массива и проверять,
какие суммы становятся достижимыми, если добавить это число к уже найденным комбинациям.
Чтобы не мешать предыдущую и нынешнюю строки, массив dp обновляется в обратном порядке (как бы плата за отказ от двухмерного dp))

После обработки всех чисел значение dp[half_sum] показывает,
можно ли набрать половину общей суммы очков.
Если это так, то оставшиеся элементы автоматически образуют вторую часть
с такой же суммой, и ответом является True.

-- ДОКАЗАТЕЛЬСТВО КОРРЕКТНОСТИ --

Алгоритм на каждом шаге поддерживает инвариант динамического программирования:
массив dp корректно отражает все суммы, которые можно составить
из уже рассмотренных чисел.
Если dp[j] = True, значит, существует подмножество среди первых элементов массива,
сумма которого равна j.

Базовое состояние задаётся правильно: сумма 0 достижима пустым множеством,
а все остальные значения изначально недостижимы.
При обработке очередного числа num значения dp[j] обновляются только на основе
предыдущих состояний dp[j - num], которые соответствуют множествам,
не включающим текущее число.

Обратный порядок обхода по j гарантирует, что каждое число используется не более одного раза,
так как при вычислении dp[j] обращение к dp[j - num] всегда происходит
к значениям из предыдущего шага, а не к уже обновлённым в этой итерации.
Таким образом, инвариант сохраняется для всех шагов цикла.

После обработки всех чисел значение dp[half_sum] показывает,
существует ли подмножество с суммой, равной половине общей суммы очков.
Если это так, то вторая часть автоматически имеет ту же сумму,
а значит, разбиение возможно, и алгоритм выдаёт корректный ответ.


-- ВРЕМЕННАЯ СЛОЖНОСТЬ --

В любом случае придется пройтись по всем N элементам, по каждому sum_i элементу из половины суммы N элементов и на каждый такой
шаг придется несколько константных операций, которые условно обозначим O(const * 1) = O(1).

Из условия знаем, что в каждой партии число заработанных очков не превосходит 300 => сумму N элементов можем представить
как 300 * N (в худшем случае) => половиной суммы назовем 150 * N. Тогда итоговая оценка:
O(N * 150N * 1) = O(N * 150N) = O(150N^2) = O(N^2)


-- ПРОСТРАНСТВЕННАЯ СЛОЖНОСТЬ --

Так как реализация основана не на двумерной матрице dp, а на запоминании только последней строки, то
пространственная сложность определяется суммой: 1) длины массива dp, равной количеству возможных сумм,
т.е. примерно 150 * N в худшем случае, 2) количества данных чисел, из которых мы и составляем суммы, т.е. N,
3) нескольких дополнительных константных переменных.

=> итоговая пространственная сложность алгоритма:

O(N + 150N + 1) = O(N + 150N) = O(151N) = O(N)

"""


def if_equal_sum(nums: list[int]) -> bool:
    total_sum = sum(nums)
    if total_sum % 2 != 0:
        return False

    half_sum = total_sum // 2
    dp = [False] * (half_sum + 1)
    dp[0] = True  # база динамики

    for num in nums:
        for j in range(half_sum, num - 1, -1):
            if dp[j - num]:  # переход
                dp[j] = True

    return dp[half_sum]


def main():
    input()
    nums = list(map(int, input().split()))
    print(if_equal_sum(nums))


if __name__ == "__main__":
    main()